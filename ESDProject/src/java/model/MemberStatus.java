package model;

/**
 * Represents the status of a member.
 * @author Matthew Carpenter 14012396
 * @author Rachel Bailey 13006455
 */
public enum MemberStatus {

    /**
     * Indicates that the member has not been approved yet.
     */
    APPLIED,

    /**
     * Indicates that the member has been approved.
     */
    APPROVED,
    
    /**
     * Indicates that the member has been suspended.
     */
    SUSPENDED
      
}